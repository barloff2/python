# 03 - casting types
# transformar un tipo de un valor a otro
###

print("conversion de tipos")
print(type(int("100")))

print("100" + str(2))
print(2 + int("100"))

print(type(float("3.1416")))
print(type(int(3.1416)))

print(bool(3))
print(bool(0))
print(bool(-1))

print(bool(""))
print(bool(" "))
print(bool("False"))

print(int("3"))
print(round(4.6))
